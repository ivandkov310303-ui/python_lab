# Создание списка из 5 материалов
materials = ["Кирпич", "Цемент", "Песок", "Арматура", "Бетон"]
def main() -> None:
    """Демонстрирует операции со списком материалов."""
    print("=== КАТАЛОГ МАТЕРИАЛОВ ===")
    print(f"Начальный список: {materials}")

    # Вывод первого, последнего и средних элементов
    print(f"\nПервый элемент: {materials[0]}")
    print(f"Последний элемент: {materials[-1]}")
    print(f"Средние элементы: {materials[1:3]}")

    # Добавление 2 новых материалов
    materials.append("Гравий")
    materials.append("Доска")
    print(f"\nПосле добавления: {materials}")

    # Удаление второго элемента (индекс 1)
    removed = materials.pop(1)
    print(f"Удалён элемент: {removed}")

    # Вывод итогового списка и длины
    print(f"\nИтоговый список: {materials}")
    print(f"Длина списка: {len(materials)} элементов")

if __name__ == "__main__":
    main()