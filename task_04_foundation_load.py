# Номер дня недели (1-7)
day_number = 3
def get_day_name(day: int) -> str:
    """Возвращает название дня недели по номеру."""
    days = {
        1: "Понедельник",
        2: "Вторник",
        3: "Среда",
        4: "Четверг",
        5: "Пятница",
        6: "Суббота",
        7: "Воскресенье"
    }
    return days.get(day, "Неизвестный день")

def main() -> None:
    """Определяет и выводит день недели и режим работы."""
    if day_number < 1 or day_number > 7:
        print("Ошибка: номер дня должен быть от 1 до 7")
        return

    day_name = get_day_name(day_number)

    print("=== РАБОЧИЙ ГРАФИК ===")
    print(f"День недели: {day_name}")

    if day_number <= 5:
        print("Режим: РАБОЧИЙ день")
        print("8:00 - начало смены")
    else:
        print("Режим: ВЫХОДНОЙ день")
        print("Отдых")

if __name__ == "__main__":
    main()