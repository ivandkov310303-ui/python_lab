# Создание словаря с 5 материалами и ценами
prices = {
    "Кирпич": 12.50,
    "Цемент": 450.00,
    "Песок": 800.00,
    "Арматура": 48000.00,
    "Бетон": 4200.00
}
def main() -> None:
    """Демонстрирует операции со словарём материалов и цен."""
    print("=== ПРАЙС-ЛИСТ МАТЕРИАЛОВ ===")
    print(f"Начальный прайс-лист: {prices}")

    # Добавление 2 новых материалов
    prices["Гравий"] = 1500.00
    prices["Доска"] = 850.00
    print(f"\nПосле добавления: {prices}")

    # Изменение цены одного (+10%)
    old_price = prices["Цемент"]
    prices["Цемент"] = old_price * 1.10
    print(f"\nЦена Цемента изменена: {old_price:.2f} → {prices['Цемент']:.2f} руб (+10%)")

    # Удаление одного материала
    removed = prices.pop("Песок")
    print(f"Удалён материал 'Песок' с ценой {removed:.2f} руб")

    # Расчёт средней цены
    average_price = sum(prices.values()) / len(prices)
    print(f"\nСредняя цена: {average_price:.2f} руб")

    # Итоговый словарь
    print(f"\nИтоговый прайс-лист: {prices}")

if __name__ == "__main__":
    main()